// Variables and Datatypes
// Find the perimeter of a rectangle, Its width is 5 and Its height is 6, assign the result to perimeter variable and then print the perimeter variable 
// Output : Perimeter = 22
package main

import "fmt"

func main() {
    width := 5
    height := 6
    perimeter := 2 * (width + height)
    fmt.Println("Perimeter =", perimeter)
}
